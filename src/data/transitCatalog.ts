type LineSeed = {
  id: string;
  mode: TransportLine['mode'];
  code: string;
  name: string;
  color: string;
  fareTnd: number;
  origin: string;
  destination: string;
  durationMinutes: number;
  intervalMinutes: number;
  routeLabel: string;
  stopNames: string[];
  firstDeparture: string;
  operatorName: string;
  servicePattern: string;
  verificationNotes?: string;
  sourceLabel: string;
};

const BASE_SOURCE_LABEL = 'Tunismapper line detail pages cross-checked with Transtu line directory';

function slugify(value: string) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 48);
}

function clockToMinutes(clock: string) {
  const [hours, minutes] = clock.split(':').map(Number);
  return hours * 60 + minutes;
}

function minutesToClock(totalMinutes: number) {
  const normalized = ((totalMinutes % (24 * 60)) + 24 * 60) % (24 * 60);
  const hours = Math.floor(normalized / 60);
  const minutes = normalized % 60;
  return `${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}`;
}

function pickMajorStops(stopNames: string[]) {
  if (stopNames.length <= 5) {
    return new Set(stopNames);
  }

  return new Set([
    stopNames[0],
    stopNames[Math.floor(stopNames.length / 3)],
    stopNames[Math.floor((stopNames.length * 2) / 3)],
    stopNames[stopNames.length - 1],
  ]);
}

function buildStops(lineId: string, stopNames: string[], firstDeparture: string, durationMinutes: number) {
  const majorStops = pickMajorStops(stopNames);
  const startMinutes = clockToMinutes(firstDeparture);
  const step = stopNames.length > 1 ? durationMinutes / (stopNames.length - 1) : 0;

  return stopNames.map(
    (name, index) =>
      ({
        id: `${lineId}-${slugify(name)}`,
        name,
        plannedTime: minutesToClock(startMinutes + Math.round(step * index)),
        etaMinutes: Math.max(0, Math.round(step * index)),
        isMajor: majorStops.has(name),
      }) satisfies LineStop,
  );
}

function createLine(seed: LineSeed): TransportLine {
  return {
    id: seed.id,
    mode: seed.mode,
    code: seed.code,
    name: seed.name,
    color: seed.color,
    fareTnd: seed.fareTnd,
    origin: seed.origin,
    destination: seed.destination,
    durationMinutes: seed.durationMinutes,
    intervalMinutes: seed.intervalMinutes,
    routeLabel: seed.routeLabel,
    stops: buildStops(seed.id, seed.stopNames, seed.firstDeparture, seed.durationMinutes),
    operatorName: seed.operatorName,
    servicePattern: seed.servicePattern,
    verificationNotes: seed.verificationNotes,
    sourceLabel: seed.sourceLabel,
  };
}

function stopId(lineId: string, stopName: string) {
  return `${lineId}-${slugify(stopName)}`;
}

